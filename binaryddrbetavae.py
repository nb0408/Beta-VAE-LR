    print(f"| Avg | {avg_metrics['Accuracy']:.4f} | {avg_metrics['F1 Score']:.4f} | {avg_metrics['Precision']:.4f} | {avg_metrics['Recall']:.4f} | {avg_metrics['Sensitivity']:.4f} | {avg_metrics['Specificity']:.4f} |")
